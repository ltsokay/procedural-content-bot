    _PERM = _PERM * 2  #Дублируем

    def _fade(t: float) -> float:
        #Сглаживающая кривая 6t^5 - 15t^4 + 10t^3 — делает переходы плавными
        return t * t * t * (t * (t * 6 - 15) + 10)

    def _lerp(a: float, b: float, t: float) -> float:
        #Линейная интерполяция между a и b
        return a + t * (b - a)

    def _grad(hash_value: int, x: float, y: float) -> float:
        #Превращаем хэш в один из 4 диагональных градиентов
        h = hash_value & 3
        u = x if h < 2 else y
        v = y if h < 2 else x
        return (u if (h & 1) == 0 else -u) + (v if (h & 2) == 0 else -v)

    def pnoise2(x: float, y: float) -> float:
        #Координаты клетки решетки
        xi = int(math.floor(x)) & 255
        yi = int(math.floor(y)) & 255
        #Дробные части внутри клетки
        xf = x - math.floor(x)
        yf = y - math.floor(y)
        #Сглаженные веса
        u = _fade(xf)
        v = _fade(yf)
        #Хэши четырех углов клетки
        aa = _PERM[_PERM[xi] + yi]
        ab = _PERM[_PERM[xi] + yi + 1]
        ba = _PERM[_PERM[xi + 1] + yi]
        bb = _PERM[_PERM[xi + 1] + yi + 1]
        #Интерполируем градиенты углов и получаем итоговое значение
        x1 = _lerp(_grad(aa, xf, yf), _grad(ba, xf - 1, yf), u)
        x2 = _lerp(_grad(ab, xf, yf - 1), _grad(bb, xf - 1, yf - 1), u)
        return _lerp(x1, x2, v)

